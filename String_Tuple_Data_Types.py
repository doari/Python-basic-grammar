# 튜플은 데이터의 변경이 불가능하므로, 리스트와 다르게 키 값으로 사용된 수 있습니다.
# 리스트보다 메모리를 효율적으로 사용하고 간편하게 데이터를 관리할 수 있습니다.
# 튜플은 한 번 선언된 값을 변경할 수 없습니다.
